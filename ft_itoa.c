#include <libft.h>

char	*ft_itoa(int n)
{
	char	s[12];
	int		i;
	int		sign;

	i = 11;
	s[i] = '\0';
	if (n == 0)
		s[--i] = '0';
	if (n < 0)
		sign = -1;
	while (n)
	{
		if (n > 0)
			s[--i] = 48 + (n % 10);
		if (n < 0)
			s[--i] = 48 - (n % 10);
		n /= 10;
	}
	if (sign == -1)
		s[--i] = '-';
	return (ft_strdup(&s[i]));
}
